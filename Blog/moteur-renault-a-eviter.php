<h2 id="1-2-tce">Le pire moteur essence Renault : le 1.2 TCe (H5Ft)</h2>
<p>S'il n'y avait qu'un seul code moteur à rayer de votre liste, c'est bien le H5Ft.</p>

<h3>Pourquoi c'est le "Motorgate" absolu ?</h3>
<p>Ce petit 4-cylindres turbo devait représenter le summum du downsizing chez Renault, mais il s'est transformé en véritable cauchemar pour des milliers de propriétaires. En effet, ce bloc souffre d'un défaut de conception majeur au niveau des segments de pistons, ce qui entraîne une surconsommation d'huile délirante, pouvant dépasser 0,5 à 1 litre tous les 1 000 kilomètres.</p>
<p>Par conséquent, cette huile brûlée encrasse les soupapes d'admission et provoque une perte de puissance. De plus, la chaîne de distribution a tendance à se détendre prématurément. Le résultat final est souvent fatal : la casse moteur pure et simple, parfois dès 50 000 km. Face à l'ampleur du désastre, des actions collectives (class actions) ont même été soutenues par l'UFC-Que Choisir en France.</p>

<h3>Quels sont les modèles équipés du 1.2 TCe à éviter ?</h3>
<p>L'Alliance Renault-Nissan ayant partagé ses mécaniques, ce moteur toxique s'est retrouvé sous le capot de dizaines de modèles très populaires entre 2012 et 2016. Vous devez l'éviter sur :</p>
<ul>
    <li><strong>Renault :</strong> Clio 4 (115 et 120 ch), <strong><u><a href="/blog/renault-captur-modele-a-eviter">Captur 1 (120 ch)</a></u></strong>, Mégane 3 et 4, Scénic 3 et 4, Kadjar.</li>
    <li><strong>Dacia :</strong> Duster, Lodgy et Dokker (versions 115 ou 125 ch).</li>
