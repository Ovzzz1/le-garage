<h2 id="k13">Micra K13 (2010-2017) : la génération 4 cumule les faiblesses</h2>

<p>La K13 pullule sur le marché de la seconde main avec des tarifs attractifs, mais c'est clairement la génération que je déconseille le plus pour les budgets intermédiaires.</p>

<h3>Chaîne de distribution : le défaut majeur dès 60 000 km</h3>
<p>Je vois régulièrement arriver des K13 de 2010 ou 2011 avec un bruit métallique très net au ralenti et au démarrage. Ce claquement indique que la chaîne de distribution s'est détendue, et ce problème survient de façon anormale dès 60 000 kilomètres sur ces modèles. C'est un défaut majeur qui impose une intervention lourde et coûteuse.</p>

<h3>Le gouffre financier de la boîte auto CVT Xtronic</h3>
<p>Si vous essayez une Micra K13 automatique et que vous ressentez des à-coups ou une perte de puissance, fuyez immédiatement. La boîte CVT Xtronic est un gouffre financier absolu car sa durée de vie est ridicule. Le remplacement de cette transmission franchit souvent la barre des 3000 euros, ce qui ruine totalement l'intérêt d'acheter cette voiture d'occasion.</p>

<h3>Fiabilité du moteur 1.2 essence : prudence sur les premiers millésimes</h3>
<p>Il faut séparer le bon du mauvais sur ce bloc. Le moteur 1.2 atmosphérique produit après 2013 s'avère plutôt robuste, par contre vous devez faire preuve d'une grande prudence sur les modèles sortis entre 2010 et 2012 où la fiabilité globale n'est pas au rendez-vous.</p>

<h2 id="k12">Micra K12 (2003-2010) : attention aux pannes électriques et capteurs</h2>

<p>Pour les petits budgets, la silhouette ronde de la K12 attire l'œil, cependant c'est un véritable nid à pannes immobilisantes.</p>

<h3>Capteur d'arbre à cames : le coupable des pertes de puissance</h3>
<p>Le ralenti instable, les ratés d'allumage et les difficultés à démarrer sont le quotidien de beaucoup de propriétaires de K12. Lors de mes diagnostics, le coupable est presque toujours le capteur d'arbre à cames. Une fois défaillant, le calculateur perd le nord et la voiture subit des pertes de puissance dangereuses.</p>

<h3>Sonde lambda, démarreur et batterie qui se vident</h3>
<p>Ajoutez à cela un faisceau électrique capricieux qui ruine le budget entretien. La sonde lambda lâche fréquemment, tandis que des bugs électroniques pompent l'énergie à l'arrêt, vidant la batterie et fatiguant le démarreur prématurément.</p>

<h2 id="k14">Micra K14 (2017-2026) : une Micra 5 plus solide, mais avec des exceptions</h2>

<p>La cinquième génération redresse la barre et propose une conception bien plus sérieuse, mais il reste des pièges à éviter.</p>

<h3>Les bugs de jeunesse à vérifier sur les modèles 2017-2018</h3>
<p>Les premiers millésimes de la K14 ont essuyé les plâtres avec un système d'infodivertissement souvent défaillant et de petits bugs électriques. Assurez-vous que l'ancien propriétaire a bien effectué les mises à jour et les rappels constructeur en concession.</p>

<h3>Les moteurs à privilégier : focus sur le 1.0 IG-T (100 et 117 ch)</h3>
