<h2 id="diesel-dci">Nissan Qashqai diesel : Les modèles dCi à surveiller de près</h2>
<p>Nissan a longtemps profité de l'expertise de l'Alliance avec Renault pour ses moteurs diesels (dCi). S'ils sont globalement plus robustes que le 1.2 DIG-T, certains millésimes exigent une vigilance particulière lors de l'achat.</p>

<h3>Le 1.5 dCi 110 ch (Problèmes FAP et injecteurs)</h3>
<p>Le 1.5 dCi est un moteur extrêmement répandu, mais sur les Qashqai de première génération (avant 2012), il a connu des défaillances récurrentes. Les <strong>injecteurs Delphi</strong> ont tendance à gripper, entraînant des factures salées (souvent plus de 1 200 euros). De plus, ce moteur supporte très mal les petits trajets urbains : le filtre à particules (FAP) s'encrasse rapidement, tout comme la vanne EGR. À réserver strictement aux gros rouleurs.</p>

<h3>Le 1.6 dCi 130 ch (Fiable mais quelques faiblesses turbo)</h3>
<p>Plus performant, le 1.6 dCi de 130 chevaux est globalement un bien meilleur choix. Cependant, sur les modèles produits entre 2011 et 2014, quelques propriétaires ont remonté une <strong>fragilité du turbo</strong>, qui peut siffler et rendre l'âme avant 100 000 kilomètres. Si vous optez pour ce modèle, exigez un carnet d'entretien scrupuleux prouvant que les vidanges ont été faites en temps et en heure.</p>

<h2 id="xtronic">Boîte automatique X-Tronic (CVT) : Fragilité et reprogrammation</h2>
<p>Le Nissan Qashqai n'utilise pas une boîte automatique traditionnelle, mais une <strong>transmission à variation continue (CVT) appelée X-Tronic</strong>. Si elle offre une conduite souple, sa fiabilité à long terme est pointée du doigt par de nombreux propriétaires.</p>

<h3>Quels sont les symptômes d'une boîte X-Tronic à éviter ?</h3>
<p>La boîte X-Tronic a souvent été victime d'à-coups, de pertes de puissance, voire de ruptures de la courroie métallique interne. Si vous repérez ces symptômes à l'essai, fuyez :</p>
<ul>
    <li>Des "trous" à l'accélération.</li>
    <li>Un patinage excessif (le moteur hurle mais la voiture n'avance pas).</li>
    <li>Des vibrations anormales à basse vitesse.</li>
</ul>

