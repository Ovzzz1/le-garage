<h3>Le cas spécifique du Diesel : Les moteurs 1.6 D (Peugeot) vs 2.0 D (BMW)</h3>
<p>Sur le marché de l'occasion, les modèles Cooper D d'avant 2011 utilisent le moteur 1.6 HDI d'origine PSA. Bien que sobre, ce bloc souffre de fuites d'injecteurs et de ruptures de turbo si l'entretien n'est pas scrupuleux. De surcroît, le filtre à particules (FAP) s'encrasse rapidement lors des trajets urbains. À partir de 2011, l'arrivée du moteur 2.0 D d'origine BMW a amélioré la donne, toutefois, il reste conseillé d'éviter les diesels pour un usage strictement citadin afin de limiter les frais de maintenance liés à la dépollution.</p>

