<h2 id="whitelist">Nos recommandations claires : Quels modèles de 308 acheter ?</h2>

<h3>Le 2.0 BlueHDi 150 ch ou 180 ch : Le diesel de référence</h3>
<p>C'est la motorisation diesel la plus aboutie de la 308 II. Robuste, économique sur autoroute, ce bloc excelle dans un usage grand routier. Si votre budget le permet (il est plus cher à l'achat), c'est le meilleur pari sur le marché de l'occasion pour une 308 diesel. Assurez-vous que le carnet d'entretien est complet et que la distribution a été vérifiée.</p>

<h3>Le 1.2 PureTech post-2021 (308 III à chaîne) : L'essence récupéré</h3>
<p>La 308 III corrige enfin le talon d'Achille du PureTech en passant à une distribution par chaîne. C'est un changement fondamental. Attention cependant : quelques propriétaires signalent encore une consommation d'huile légèrement supérieure à la normale. Surveillez le niveau à chaque plein les premiers mois. Sur cette base, c'est néanmoins un achat raisonnable, surtout sur les millésimes 2022 et ultérieurs.</p>

<h3>La 308 II phase 2 (2018–2021) : La génération intermédiaire fiable</h3>
<p>Souvent oubliée dans le débat, la <strong>308 II phase 2</strong> (restylage de 2017–2018) représente le meilleur compromis de toute la gamme occasion. À cette période, Peugeot avait déjà corrigé la majorité des défauts de jeunesse du PureTech, et les blocs 1.6 THP avaient été fiabilisés (versions 125 et 155 ch post-2012). Le <strong>1.6 BlueHDi 120 ch</strong> de cette période est un excellent compromis fiabilité/consommation pour un usage routier régulier. Sur un budget maîtrisé, c'est probablement la meilleure 308 d'occasion accessible.</p>

<h3>La 308 a-t-elle une courroie ou une chaîne de distribution ?</h3>
<p>La réponse dépend entièrement du moteur et de l'année :</p>
<ul>
    <li><strong>1.6 THP (2007–2013) :</strong> Chaîne de distribution — mais elle s'allonge prématurément (problème principal de ce moteur).</li>
    <li><strong>1.2 PureTech (2014–2020) :</strong> Courroie de distribution baignant dans l'huile — c'est LE défaut majeur à éviter.</li>
    <li><strong>1.2 PureTech (308 III, dès 2021) :</strong> Chaîne de distribution — correction du défaut rédhibitoire.</li>
    <li><strong>1.6 et 2.0 BlueHDi :</strong> Courroie de distribution classique — à remplacer selon les préconisations constructeur (tous les 120 000 à 150 000 km).</li>
</ul>

<h3>Les finitions à privilégier</h3>
<p>Au-delà de la motorisation, le choix de la finition a son importance. Évitez la finition <strong>Access</strong> (trop dépouillée, difficile à revendre) et la <strong>GT Line</strong> avec jantes 18 pouces (pneus hors de prix, suspensions fermes). Les finitions <strong>Active</strong> et <strong>Allure</strong> offrent le meilleur rapport équipement / prix de revente.</p>

