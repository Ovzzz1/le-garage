</ul>
<p><strong>Verdict :</strong> Si vous voyez un 308 I avec 1.6 THP 150 ou 175 ch, passez votre chemin. Le rapport risque/prix ne justifie jamais cet achat.</p>

<h3>Le 1.6 HDi 92 diesel : Turbo fragile et EGR encrassée</h3>
<p>Parmi les diesels de la 308 I, le <strong>1.6 HDi 92 ch</strong> est le plus à risques sur la période 2010–2014. Le turbo manifeste des signes de faiblesse avant 100 000 km — un kilométrage anormalement bas pour un diesel. Les autres fragilités sont bien documentées :</p>
<ul>
    <li>Injecteurs défaillants : démarrages difficiles, fuites de carburant</li>
    <li>Volant moteur bi-masse usé parfois avant 40 000 km</li>
