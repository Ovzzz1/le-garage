<h2 id="308-2">La Peugeot 308 II (2014–2021) : Le modèle à éviter en PureTech</h2>
<p>La deuxième génération a apporté un vrai bond en avant en termes de qualité perçue et de design. Mais elle a aussi introduit le moteur le plus problématique de toute l'histoire de la 308.</p>

<h3>Le 1.2 PureTech 110/130 ch : Le modèle à éviter à tout prix (2014–2018)</h3>
<p>Voilà probablement le moteur qui a le plus endommagé la réputation de Peugeot ces dix dernières années. Le <strong>1.2 PureTech</strong> des premières années souffre d'un défaut de conception majeur : la courroie de distribution baigne dans l'huile et se dégrade de façon accélérée. Les particules de caoutchouc issues de cette dégradation viennent boucher la crépine de la pompe à huile. Résultat : une mauvaise lubrification du moteur qui peut mener à une <strong>casse moteur totale sans signe avant-coureur</strong>.</p>
<ul>
    <li>Courroie de distribution dans l'huile : dégradation parfois avant 60 000 km</li>
    <li>Consommation d'huile pouvant atteindre 1 litre aux 1 000 km</li>
    <li>Dilution de l'huile par l'essence lors de trajets courts répétés</li>
    <li>Casse moteur totale : facture pouvant atteindre <strong>7 000 à 8 000 euros</strong></li>
</ul>
<p>Fin 2020, Peugeot a lancé un rappel massif concernant plus de <strong>220 000 véhicules en France</strong>. Mais malgré les interventions, les problèmes persistent sur certains modèles. Si vous achetez un 308 II avec ce moteur, exigez impérativement la preuve que le rappel constructeur a bien été effectué et que le carnet d'entretien est complet.</p>

<h3>Les BlueHDi diesel de la 308 II : des complications à anticiper</h3>
<p>Les <strong>1.6 BlueHDi</strong> et <strong>2.0 BlueHDi</strong> sont globalement plus fiables que les versions essence PureTech, mais pas sans défauts. En usage urbain principalement, ils montrent des signes de fragilité qui peuvent devenir coûteux :</p>
<ul>
    <li>FAP qui s'encrasse rapidement (régénérations imposées en conduite sur route)</li>
    <li>Injecteurs fragiles à 400 euros pièce</li>
    <li>Système AdBlue capricieux : cristallisation du liquide possible</li>
    <li>Pompe haute pression défaillante : réparations dépassant 1 000 euros</li>
</ul>
<p>Notre conseil : évitez ces diesels si vous roulez principalement en ville. En revanche, pour des longs trajets réguliers, le <strong>2.0 BlueHDi 150 ch</strong> reste la motorisation diesel la plus aboutie et la plus fiable de cette génération.</p>

