<h2 id="308-1">La Peugeot 308 I (2007–2013) : Les modèles à éviter</h2>
<p>La première génération a posé les bases du succès de la 308, mais elle traîne une réputation mécaniquement fragile qui se confirme encore aujourd'hui sur le marché de l'occasion.</p>

<h3>Le moteur 1.6 THP : Le modèle à éviter absolument en essence</h3>
<p>Développé en partenariat avec BMW, le <strong>1.6 THP</strong> semblait prometteur sur le papier. Dans les faits, il est devenu l'un des moteurs les plus problématiques de cette génération. Le point faible fatal : la <strong>chaîne de distribution</strong>. Elle s'allonge prématurément dès 60 000 kilomètres, parfois moins. Le tendeur hydraulique défaillant peut provoquer un décalage de la distribution et entraîner une <strong>casse moteur complète</strong>, avec une facture qui peut dépasser <strong>3 000 à 5 000 euros</strong>.</p>
<ul>
