<h3>À-coups à froid et broutages (Surtout 2012-2016)</h3>
<p>Beaucoup de conducteurs signalent des "trous" à l'accélération et un broutage désagréable lors des démarrages à froid, surtout par temps humide. Ce n'est pas une casse mécanique, mais un problème de gestion électronique. Une simple mise à jour de la cartographie en concession règle souvent ce comportement erratique.</p>

<h3>Bruit métallique de wastegate et fatigue du turbo</h3>
<p>Lors des décélérations, il n'est pas rare d'entendre un bruit de ferraille ou un cliquetis venant du compartiment moteur. Ce bruit provient de la tige de la "wastegate" (la soupape de décharge du turbo) qui prend du jeu. C'est agaçant d'un point de vue acoustique, mais cela ne nécessite généralement pas le remplacement immédiat du turbo.</p>

<h3>Caprices électroniques : Capteur PMH et Sondes Lambda</h3>
<p>Le 0.9 TCe n'échappe pas aux petits aléas de l'électronique moderne. Des sondes Lambda défectueuses ou un capteur PMH (Point Mort Haut) encrassé peuvent allumer le voyant moteur au tableau de bord, voire empêcher le démarrage. Ce sont des pièces d'usure périphériques dont le remplacement est rapide et relativement peu coûteux.</p>

