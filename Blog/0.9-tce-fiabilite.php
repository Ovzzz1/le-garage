<h3>La maladie n°1 : Fuite du boîtier d'eau (Thermostat)</h3>
<p>C'est le talon d'Achille de ce moteur. Le boîtier thermostatique est en plastique et finit souvent par se fissurer avec les cycles de chaleur. Le symptôme est clair : une baisse inexpliquée du niveau de liquide de refroidissement dans le bocal, ou des traces blanchâtres sur le moteur. C'est une panne à traiter d'urgence car rouler sans liquide entraîne une casse du joint de culasse.</p>
<ul>
    <li><strong>Coût estimé :</strong> Entre 200 et 450 euros selon le garage.</li>
