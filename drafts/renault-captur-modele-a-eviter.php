<h3>1.2 TCe 120 ch (2013-2018) : Le défaut de conception majeur</h3>
<p>C'est le moteur le plus problématique de l'histoire du Captur, portant le nom de code <strong>H5Ft</strong>. Le défaut principal réside dans une mauvaise gestion de la pression d'admission qui provoque une consommation d'huile moteur excessive (souvent plus d'un litre pour 1000 km). Ce manque de lubrification entraîne irrémédiablement une usure de la chaîne de distribution, voire une casse moteur totale. Ce scandale, souvent appelé <strong>"Motorgate"</strong>, a touché des milliers d'utilisateurs. Ce même moteur H5Ft, rebaptisé 1.2 DIG-T chez Nissan, provoque exactement les mêmes casses sur le <strong><u><a href="/brouillon/nissan-juke-modele-a-eviter">Nissan Juke</a></u></strong> de 2014 à 2018 — une information utile si vous hésitez entre les deux modèles. Si vous voyez une annonce pour un Captur essence de cette période avec un prix anormalement bas, la raison est là. Contrairement aux discours de certains vendeurs, une simple reprogrammation moteur ne suffit pas toujours à régler un problème mécanique structurel.</p>

